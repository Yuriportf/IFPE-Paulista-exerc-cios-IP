"""Leia uma temperatura em graus Celsius e apresente-a convertida em graus Kelvin. A
formula de converçao é: K = C + 273.15, sendo C a temperatura em Celsius e K a
temperatura em Kelvin."""

Gcelsius = float(input("Digite seus Graus Celsius para converção aqui: "))
converkelvin = (Gcelsius + 273.15)
print(f"A temperatura em Kelvin é {converkelvin}")
